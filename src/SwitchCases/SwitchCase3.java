package SwitchCases;

public class SwitchCase3 {

	public static void main(String[] args) {
		char ch='a';
		
		switch(ch) {
		case 'a':
			System.out.println("Case 1");
			break;
			
		case 'b':
			System.out.println("Case 2");
			break;
			
			default:
				System.out.println("Default");
		}

	}

}
